/// Built-in animation styles.
enum AwesomeAnimation {
  /// Slides in from the nearest edge.
  slide,

  /// Fades in/out smoothly.
  fade,

  /// Scales from center with ease-out-back.
  scale,

  /// Spring-like elastic entrance.
  elastic,

  /// Bouncy entrance.
  bounce,

  /// Rotation + scale + fade.
  rotation,

  /// iOS-style cubic-emphasized curve.
  ios,

  /// Flip entrance (3D card flip).
  flip,

  /// Glide in from a custom path.
  path,
}
